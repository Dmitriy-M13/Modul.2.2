week = int(input("Введите номер дня недели от 1 до 7: "))

if week == 1:
    print('Понедельник')
if week == 2:
    print('Вторник')
if week == 3:
    print('Среда')
if week == 4:
    print('Четверг')
if week == 5:
    print('Пятница')
if week == 6:
    print('Суббота')
if week == 7:
    print('Воскресенье')
